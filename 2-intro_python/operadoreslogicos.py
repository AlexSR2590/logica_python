"""
El operador and devuelve True cuando todos los elementos
son True.
"""
#Ejemplo operador and
print("------ Ejemplo operador and ------")
print(f"{True} and {True} = {True and True}")#Devuelve True
print(f"{True} and {False} = {True and False}")#Devuelve False

""" El operador or devuelve True cuando almenos uno
de los elementos es True """
print("------ Ejemplo operador or ------")
print(f"{True} or {True} = {True or True}")#Devuelve True
print(f"{True} or {False} = {True or False}")#Devuelve True

""" El operador not invierte el True por False y 
False por True """
print("------ Ejemplo operador not ------")
print(f"not {True} = {not True}")#Devuele False
print(f"not {False} = {not False}")#Devuelve True
