"""Operadores aritméticos en Python"""
suma = 8 + 4
resta = 8 - 4
multiplicacion = 8 * 4
division = 8 / 4
modulo = 8 % 5
div_entero = 8 // 5
potencia = 8 ** 3

#Imprimiendo los resultados de cada operación 
print(f"suma 8 + 4 = {suma}")
print(f"resta 8 - 4 = {resta}")
print(f"multiplicación 8 * 4 = {multiplicacion}")
print(f"division 8 / 4 = {division}")
print(f"modulo 8 % 5 = {modulo}")
print(f"division entero 8 // 5 = {div_entero}")
print(f"potencia 8 ** 3 = {potencia}")




